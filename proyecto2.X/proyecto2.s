    MOVLW 0xF0
    ANDWF ALARMA_MH, F
    SWAPF ALARMA_MH, F
    
    MOVLW 0x0F
    ANDWF ALARMA_HL, F
    
    MOVLW 0xF0
    ANDWF ALARMA_HHAH, F
    SWAPF ALARMA_HH, F
    
  
   
LOOP:

