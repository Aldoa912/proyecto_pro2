PSECT CODE, delta=2, abs
 ORG 0x0004
 PUSH:
    MOVWF W_TEMP
    SWAPF STATUS, W
    MOVWF STATUS_TEMP

ISR:
    BTFSC   RBIF
    GOTO    ISRRBIF
    BTFSC   TMR1IF
    GOTO    ISRTMR1
    BTFSC   TMR0IF
    GOTO    ISRTMR0
    GOTO    SALIDA
    
ISRRBIF:
    ;BTFSS   INTCON, 0	    ; RBIF = 1 ?
    ;GOTO    ISRTMR1	    ; SI NO ESTA ENCENDIDO VAMOS A POP
    BANKSEL PORTB
    BTFSS   PORTB, 0
    INCF    CONTADOR_ES, F
    BCF	    RBIF
    GOTO    MOD_HORA
    GOTO    SALIDA
    
MOD_HORA:
    BTFSS PORTB, 1
    BSF FLAG, 1
    BTFSS PORTB, 2
    BSF FLAG, 2
    BTFSS PORTB, 3
    BSF FLAG, 3
    BTFSS PORTB, 4
    BSF FLAG, 4
    GOTO SALIDA

    

ISRTMR1:
    ;BTFSS PIR1, 0	    ; TMR1IF = 1?
    ;GOTO ISR
    BCF PIR1, 0		    ; Borramos la bandera del TMR1IF
    MOVLW 0x7C
    MOVWF TMR1L
    MOVLW 0xE1
    MOVWF TMR1H
    INCF NL
    
ISRTMR0:
    ;BTFSS INTCON,2	   ; T0IF = 1 ?
    ;GOTO SALIDA
    BANKSEL TMR0
    MOVLW 220
    MOVWF TMR0		; CARGAMOS EL VALOR DE N = DESBORDE 50mS
    BCF T0IF		; Borramos bandera T0IF 
    INCF cont10ms, F
    ;GOTO POP
    GOTO DIS0
    
DIS0:
    BANKSEL PORTA
    MOVF CONT_DIS, W
    SUBLW 0		    ; REALIZAMOS UNA COMPARACION DEL VALOR DE CONTADOR
			    ; SI ESTA ES 0 SEGUIMOS EN ESTA SUBRUTINA, SINO
			    ; PASAMOS A ESTADO01_ISR
    BTFSS STATUS, 2
    GOTO DIS1
    MOVLW 0b00000001
    MOVWF PORTA
    INCF CONT_DIS
    BCF INTCON, 0
    GOTO SALIDA
    
DIS1:
    MOVF CONT_DIS, W
    SUBLW 1		    ; REALIZAMOS UNA COMPARACION DEL VALOR DE CONTADOR
			    ; SI ESTA ES 0 SEGUIMOS EN ESTA SUBRUTINA, SINO
			    ; PASAMOS A ESTADO01_ISR
    BTFSS STATUS, 2
    GOTO DIS2
    MOVLW 0b00000010
    MOVWF PORTA
    INCF CONT_DIS
    BCF INTCON, 0
    GOTO SALIDA

DIS2:
    MOVF CONT_DIS, W
    SUBLW 2		    ; REALIZAMOS UNA COMPARACION DEL VALOR DE CONTADOR
			    ; SI ESTA ES 0 SEGUIMOS EN ESTA SUBRUTINA, SINO
			    ; PASAMOS A ESTADO01_ISR
    BTFSS STATUS, 2
    GOTO DIS3
    MOVLW 0b00000100
    MOVWF PORTA
    INCF CONT_DIS
    BCF INTCON, 0
    GOTO SALIDA
DIS3:
    MOVF CONT_DIS, W
    SUBLW 3		    ; REALIZAMOS UNA COMPARACION DEL VALOR DE CONTADOR
			    ; SI ESTA ES 0 SEGUIMOS EN ESTA SUBRUTINA, SINO
			    ; PASAMOS A ESTADO01_ISR
    BTFSS STATUS, 2
    GOTO DIS4
    MOVLW 0b00001000
    MOVWF PORTA
    INCF CONT_DIS
    BCF INTCON, 0
    GOTO SALIDA
    
DIS4:
    MOVF CONT_DIS, W
    SUBLW 4		    ; REALIZAMOS UNA COMPARACION DEL VALOR DE CONTADOR
			    ; SI ESTA ES 0 SEGUIMOS EN ESTA SUBRUTINA, SINO
			    ; PASAMOS A ESTADO01_ISR
    BTFSS STATUS, 2
    GOTO DIS5
    MOVLW 0b00010000
    MOVWF PORTA
    INCF CONT_DIS
    BCF INTCON, 0
    GOTO SALIDA
    
DIS5:

    MOVLW 0b00100000
    MOVWF PORTA
    CLRF CONT_DIS
    ;GOTO POP
    

SALIDA:
    NOP
POP:
    SWAPF STATUS_TEMP, W
    MOVWF STATUS
    SWAPF W_TEMP, F
    SWAPF W_TEMP, W
    RETFIE
