MAIN:
    BANKSEL OSCCON
    
    BCF OSCCON, 6	; IRCF2 SelecciÃƒÂ³n de 2MHz
    BSF OSCCON, 5	; IRCF1
    BCF OSCCON, 4	; IRCF0
    
    BSF OSCCON, 0	; SCS Reloj Interno
   
    BANKSEL ANSEL
    CLRF ANSEL
    CLRF ANSELH
    
    BANKSEL TRISC
    CLRF TRISC		; Limpiar el registro TRISB
    CLRF TRISA
    CLRF TRISD
    
    BSF TRISB, 0
    BSF TRISB, 1	; Entradas para los botones
    BSF TRISB, 2
    BSF TRISB, 3
    BSF TRISB, 4
    
    BANKSEL WPUB
    BSF WPUB, 0
    BSF WPUB, 1		; Habilitando los Pullups en RB0 y RB1
    BSF WPUB, 2
    BSF WPUB, 3
    BSF WPUB, 4
    
    BANKSEL OPTION_REG
    BCF OPTION_REG, 7	; HABILITANDO PULLUPS PUERTO B
    BCF OPTION_REG, 5	; T0CS: FOSC/4 COMO RELOJ (MODO TEMPORIZADOR)
    BCF OPTION_REG, 3	; PSA: ASIGNAMOS EL PRESCALER AL TMR0
    
    BSF OPTION_REG, 2
    BSF OPTION_REG, 1
    BCF OPTION_REG, 0	; PS2-0: PRESCALER 1:128 SELECIONADO 
    
    BANKSEL INTCON
    
    BSF INTCON, 7	; Se habilitan todas las interrupciones por el GIE
    BSF INTCON, 5	; Habilitando la interrupcion T0IE TMR0
    BSF INTCON,	3	; Se habilita la interrupciÃƒÂ³n del RBIE
    BCF INTCON, 2	; Apagamos la bandera T0IF del TMR0 
    BCF INTCON, 0
    
   
    
    BANKSEL IOCB
    BSF IOCB, 0
    BSF IOCB, 1		
    BSF IOCB, 2
    BSF IOCB, 3
    BSF IOCB, 4		; Habilitando RB0 y RB1 para las ISR de RBIE
    banksel PORTB
    movf    PORTB,W	;Es necesario escribir/leer el puerto y 
			;limpiar la bndera luego de configurar IOCB
    bcf	    RBIF
    
    BANKSEL PIE1
    BSF PIE1, 0
    
    BANKSEL PIR1
    BCF PIR1, 0
    
    BANKSEL T1CON
    BSF T1CON, 5
    BSF T1CON, 4	; Prescaler de 1:8  
    BCF T1CON, 1	; TMR1CS Fosc/4 reloj interno
    BSF T1CON, 0	; TMR1ON enable
    
    BANKSEL TMR1L
    MOVLW 0x7C
    MOVWF TMR1L
    MOVLW 0xE1
    MOVWF TMR1H
    
    
    ; ConfiguraciÃƒÂ³n TMR0

    
    BANKSEL PORTC
    CLRF PORTC		; Se limpia el puerto C D ^ A
    CLRF PORTD
    CLRF PORTA


    CLRF CONTADOR_M
    CLRF CONTADOR_ES
    CLRF CONTADOR
    CLRF CONTADOR_F
    CLRF CONTADOR_ES
    CLRF CONT_DIS
    CLRF ALARMA
    CLRF CONT20MS
    CLRF CONT_DIS
    CLRF NL
    CLRF NH
    CLRF ML
    CLRF MH
    CLRF HL
    CLRF HH
    CLRF DL
    CLRF DH
    CLRF AL
    CLRF AH
    CLRF ALARMA_ML
    CLRF ALARMA_MH
    CLRF ALARMA_HL
    CLRF ALARMA_HH
    CLRF FLAG
    MOVLW 220
    MOVWF TMR0		; CARGAMOS EL VALOR DE N = DESBORDE 50mS
    
